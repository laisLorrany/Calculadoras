

public class Operacao {
	
	public int Somar (int a, int b){
		System.out.println("+");
		return a + b;
	}
	
	public int Subtrair (int a, int b){
		System.out.println("-");
		return a - b;
	}
	
	public int Multiplicar (int a, int b){
		System.out.println("*");
		return a * b;
	}
	
	public int Dividir (int a, int b){
		System.out.println("/");
		return a / b;
	}
	
}
